/*
PROTOTIPOS
*/

/**
* \brief Evalua los valores ingresados, este SUMA el operador1 con el operador2
         y luego almacena el resultado en la variable resultadoSuma

* \param operador1 Cargara el primer valor
         operador2 Cargara el segundo valor

* \return El resultado de la SUMA
*
*/
float suma(float operador1, float operador2);


/**
* \brief Evalua los valores ingresados, este RESTA el operador1 con el operador2
         y luego almacena el resultado en la variable resultadoResta

* \param operador1 Cargara el primer valor
         operador2 Cargara el segundo valor

* \return El resultado de la RESTA
*
*/
float resta(float operador1, float operador2);


/**
* \brief Evalua los valores ingresados, este Divide el operador1 actuando de "dividendo"
         y el operador2 actuando de "divisor" luego almacena el resultado en la variable
         resultadoDivision

* \param operador1 Cargara el primer valor
         operador2 Cargara el segundo valor

* \return El resultado de la DIVISION
*
*/
float division(float operador1, float operador2);


/**
* \brief Evalua los valores ingresados, este los MULTIPLICA
         y luego almacena el resultado en la variable resultadoMultiplicacion

* \param operador1 Cargara el primer valor
         operador2 Cargara el segundo valor

* \return El resultado de la MULTIPLICACION
*
*/
float multiplicacion(float operador1, float operador2);


/**
* \brief Evalua los valores ingresados, Factorea el operador y luego
         almacena el resultado en la variable fact

* \param operador cargara el valor a factorear

* \return El resultado de la RESTA
*
*/
float factorial(float operador);


